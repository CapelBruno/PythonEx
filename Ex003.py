#Ex. Realize a soma de duas variáveis.

pri = int(input('Digite o primeiro número:'))       #Int atribui que toda a informação será um número inteiro
seg = int(input('Digite o segundo número:'))
soma = pri+seg      #Atribuição de soma de variáveis
print('A soma entre {} e {} é: {}' .format(pri, seg, soma))     #Formatação do que irá dentro das chaves: .format(conteúdo das chaves)
